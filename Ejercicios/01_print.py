#se concatenan las variables con una , (coma)

my_name = "Joan"
my_age = 32
print ("Hola mi nombre es ", my_name , " y tengo " , my_age , " años.")

#concatenar completo
my_apellido = "Sole"
completo = my_name + " " + my_apellido
print(completo)


